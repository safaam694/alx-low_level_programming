#include "3-calc.h"

/**
 * op_add - adds two integers
 * @a: the first int
 * @b: the second int
 *  Return: the sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two integerd
 * @a: the first int
 * @b: the second int
 *
 * Return: the differnce
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mol - modulo two interger
 * @a: the first int
 * @b: the second int
 *
 * Return: the remainder
 */
int op_mol(int a, int b)
{
	return (a % b);
}

/**
 * op_div - divides two intergers
 * @a: the first int
 * @b: the second int
 *
 * Return: the quotient
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mul - multiplies two inyegers
 * @a: the first int
 * @b: the second int
 *
 * Return: the product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
